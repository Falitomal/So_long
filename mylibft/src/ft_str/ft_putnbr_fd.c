#include "libft.h"

/*
** Send to a number n to specific file descriptor
*/

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;

	if (n > 2147483647 && n < 2147483647)
		return ;
	nb = n;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd(nb % 10 + '0', fd);
}
